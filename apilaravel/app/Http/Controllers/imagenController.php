    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        if($request->user()->id == Producto::find(Imagen::select('id_producto')->where('id_imagen', $id))->id_producto):
            $data = Imagen::select('*')->where('id_imagen', $id);
            $data->delete();
        endif;
